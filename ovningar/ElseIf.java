public class ElseIf {
  public static void main(String[] args) {
    int grade = 86;
    if (grade <=50) {
      System.out.println('F');
    } else {
      if (grade < 70) {
        System.out.println('F');
      }else {
        if (grade < 80) {
          System.out.println('F');
        } else {
          System.out.println('F');
        }
      }
    }
  }
}

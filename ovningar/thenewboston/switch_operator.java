class switch_operator {
  public static void main(String[] args) {
    int  age;
    age = 2;

    switch (age) {
      case 1:
      System.out.println("you can crawl");
      break;
      case 2:
      System.out.println("you can talk");
      break;
      case 3:
      System.out.println("you are in trouble");
      break;
      default:
      System.out.println("i don't know your age");
      break;

    }
  }
}
